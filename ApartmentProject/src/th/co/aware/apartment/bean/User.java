package th.co.aware.apartment.bean;

public class User {

	private int u_id;
	private int u_rid;
	private String username;
	private String password;
	private String u_fname;
	private String u_lname;
	private int u_day;
	private int u_month;
	private int u_year;
	private int r_id;

	public int getU_rid() {
		return u_rid;
	}

	public void setU_rid(int u_rid) {
		this.u_rid = u_rid;
	}

	public int getU_id() {
		return u_id;
	}

	public void setU_id(int u_id) {
		this.u_id = u_id;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getU_fname() {
		return u_fname;
	}

	public void setU_fname(String u_fname) {
		this.u_fname = u_fname;
	}

	public String getU_lname() {
		return u_lname;
	}

	public void setU_lname(String u_lname) {
		this.u_lname = u_lname;
	}

	public int getU_day() {
		return u_day;
	}

	public void setU_day(int u_day) {
		this.u_day = u_day;
	}

	public int getU_month() {
		return u_month;
	}

	public void setU_month(int u_month) {
		this.u_month = u_month;
	}

	public int getU_year() {
		return u_year;
	}

	public void setU_year(int u_year) {
		this.u_year = u_year;
	}

	public int getR_id() {
		return r_id;
	}

	public void setR_id(int r_id) {
		this.r_id = r_id;
	}

}
